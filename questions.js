      '7x1':'しちいちがしち', '7x2':'しちにじゅうし',    '7x3':'しちさんにじゅういち',
      '7x4':'しちしにじゅうはち', '7x5':'しちごさんじゅうご', '7x6':'しちろくしじゅうに',
      '7x7':'しちしちしじゅうく', '7x8':'しちはごじゅうろく', '7x9':'しちくろくじゅうさん',
      '8x1':'はちいちがはち', '8x2':'はちにじゅうろく',  '8x3':'はちさんにじゅうし',
      '8x4':'はちしさんじゅうに', '8x5':'はちごしじゅう', '8x6':'はちろくしじゅうはち',
