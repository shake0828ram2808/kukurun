      '1x4':'いんしがし',     '1x5':'いんごがご',        '1x6':'いんろくがろく',
      '1x7':'いんしちがしち', '1x8':'いんはちがはち',    '1x9':'いんくがく',
      '2x1':'にいちがに',     '2x2':'ににんがし',         '2x3':'にさんがろく',
      '2x4':'にしがはち',     '2x5':'にごじゅう',         '2x6':'にろくじゅうに',
      '2x7':'にしちじゅうし', '2x8':'にはちじゅうろく',  '2x9':'にくじゅうはち',
      '3x1':'さんいちがさん', '3x2':'さんにがろく',       '3x3':'さざんがく',
      '3x4':'さんしじゅうに', '3x5':'さんごじゅうご',    '3x6':'さぶろくじゅうはち',
      '3x7':'さんしちにじゅういち', '3x8':'さんぱにじゅうし', '3x9':'さんくにじゅうしち',
      '4x1':'しいちがし',     '4x2':'しにがはち',         '4x3':'しさんじゅうに',
      '4x4':'ししじゅうろく', '4x5':'しごにじゅう',       '4x6':'しろくにじゅうし',
      '4x7':'ししちにじゅうはち', '4x8':'しはさんじゅうに', '4x9':'しくさんじゅうろく',
      '5x1':'ごいちがご',     '5x2':'ごにじゅう',         '5x3':'ごさんじゅうご',
      '5x4':'ごしにじゅう',   '5x5':'ごごにじゅうご',    '5x6':'ごろくさんじゅう',
      '5x7':'ごしちさんじゅうご', '5x8':'ごはしじゅう',  '5x9':'ごくしじゅうご',
